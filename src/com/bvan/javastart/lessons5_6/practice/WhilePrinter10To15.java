package com.bvan.javastart.lessons5_6.practice;

/**
 * Вывести все числа от 10 до 15 с помощью цикла while.
 *
 * @author bvanchuhov
 */
public class WhilePrinter10To15 {

    public static void main(String[] args) {
        int n = 10;
        while (n <= 15) {
            System.out.println(n);
            n++;
        }
    }
}
